/** 数据结构
 * 民间定义：
 * 数据结构是数据对象，以及存在于该对象的实例和 组成实例的数据元素之间的各种联系。这些联系可以通过定义相关函数来给出。————《数据结构、算法与应用》
 * 数据结构是ADT(抽象数据类型 Abstract Data Type)的物理实现。————《数据结构与算法分析》
 * 数据结构(data structure)是计算机中存储、组织数据的方式。通常情况下，精心选择的数据结构可以带来最优效率的算法。————中文维基百科
 * 
 * 自己的角度：
 * 数据结构就是在计算机中，存储和组织数据的方式
 * 
 * 分类：
 * 队列、树、堆、数组、栈、链表、图、散列表
 */
/**算法(Algorithm)
 * 定义：
 * 一个有限指令集，每条指令的描述不依赖于语言
 * 接受一些输入(有些情况下不需要输入)
 * 产出输出
 * 一定在有限步骤之后终止
 * 
 * 通俗来说：
 * 算法就是解决问题的方法/步骤逻辑。
 * 数据结构的实现，离不开算法。
 */ 